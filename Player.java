class Player {

    private int playerNumber;
    private String name;
    private String height;
    private String weight;
    private int jerseyNo;
    private String playerCollege;
    private String playerNative;

    public void setPlayerNumber(int playerNumber) {
        this.playerNumber = playerNumber;
    }

    public int getPlayerNumber() {
        return this.playerNumber;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getHeight() {
        return this.height;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getWeight() {
        return this.weight;
    }

    public int getJerseyNo() {
        return this.jerseyNo;
    }

    public void setJerseyNo(int jerseyNo) {
        this.jerseyNo = jerseyNo;
    }

    public String getPlayerCollege() {
        return this.playerCollege;
    }

    public void setPlayerCollege(String playerCollege) {
        this.playerCollege = playerCollege;
    }

    public String getPlayerNative() {
        return this.playerNative;
    }

    public void setPlayerNative(String playerNative) {
        this.playerNative = playerNative;
    }
}
























